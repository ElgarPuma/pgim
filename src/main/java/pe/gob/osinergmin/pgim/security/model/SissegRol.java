package pe.gob.osinergmin.pgim.security.model;

public class SissegRol {	

	private String permisoPagina;
	
	private String codigoPagina;
		
	private String codigoPermiso;	
	

	public String getPermisoPagina() {
		return permisoPagina;
	}

	public void setPermisoPagina(String permisoPagina) {
		this.permisoPagina = permisoPagina;
	}

	public String getCodigoPagina() {
		return codigoPagina;
	}

	public void setCodigoPagina(String codigoPagina) {
		this.codigoPagina = codigoPagina;
	}

	public String getCodigoPermiso() {
		return codigoPermiso;
	}

	public void setCodigoPermiso(String codigoPermiso) {
		this.codigoPermiso = codigoPermiso;
	}	

	
	
}